package com.start.demo;

public class Demo1 {

    //第一个java程序，打印字符串“hello world”
    //也可以在cmd中通过javac 和 java 命令编译成class文件，然后执行
    public static void main(String[] args) {
        System.out.println("hello world");
    }

    /*基本语法
    * 1、大小写敏感  hello与Hello是两个截然不同的文件
    * 2、类名：采用驼峰命名法，类名首字母大写，如果由若干个单词组成，例如：“MyFirstDemo.java”
    * 3、方法名：所有方法名首字母小写，如果方法名由若干个单词组成，例如“myFirstDemo”
    * 4、源文件名：源文件名与类名相同，使用类名作为文件名保存，后缀为“.java”
    * */

    /*java标识符
    * 1、java所有组成部分都需要名字，方法名，类名，变量名都被称为标识符
    * 2、所有标识符都应已字母、美元符、下划线开始，然后才能与数字的任何字符组合
    * */

    /*java修饰符
    * 1、java开始使用修饰符来修饰类中方法和属性，主要有2类修饰符：
    *   1）、访问控制修饰符：default，public，protected，private
    *   2）、非访问控制修饰符：final，abstract，static，synchronize
    * */

    /*java 变量
    * 1、java中主要变量有如下几种类型的变量：
    *   1）、成员变量（非静态变量）
    *   2）、局部变量
    *   3）、类变量（静态变量）
    * */

    /*java数组
    * java枚举
    *java关键字
    *java空行：空行或注释行，java编译器都会忽略编译
    * */

    /*继承
    *继承父类，重用父类方法和属性
    * 被继承的类称为超类（super class），派生类称为子类（subclass)。
    * */

    /*接口
    * 接口可以理解为对象间相互通信的协议，接口只定义派生要用到的方法，但是方法的具体实现完全取决于派生类。
    * */



}
